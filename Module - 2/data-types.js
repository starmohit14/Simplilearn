//number
var x = 10;
console.log(typeof x);
console.log(x);

//string
var y = "Mohit Sharma"
console.log(typeof y);
console.log(y);

//Array
var cars = ["Mohit", "Abhishek", "Om"];
console.log(typeof cars);
console.log(cars);

//Object
var obj = {Name : 'Mohit', Place : 'Danpur'};
console.log(typeof obj);
console.log(obj);

//Undefined
var z;
console.log(typeof z);
console.log(z);

//Null
var n = null;
console.log(typeof n);
console.log(n);